#Tampilkan 5 baris pertama dari Dataframe mtcars
head(mtcars,5)

#Tampilkan 5 baris terakhir dari Dataframe mtcars
tail(mtcars,5)

# Teori
# Function head(nama_dataframe, jumlah_baris) dapat digunakan untuk menampilkan beberapa baris awal di Dataframe dan function tail(nama_dataframe, jumlah_baris) dapat digunakan untuk menampilkan beberapa baris terakhir di Dataframe.

# Instruksi :

# Gunakan function head() dan tail() pada Dataframe mtcars untuk menampilkan beberapa baris data pertama dan terakhir dari mtcars.