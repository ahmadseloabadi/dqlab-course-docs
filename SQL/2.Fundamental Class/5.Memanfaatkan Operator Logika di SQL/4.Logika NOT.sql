SELECT 
	*
FROM data_retail
WHERE NOT product = 'Jaket';

-- --Teori
-- Logika NOT dalam SQL pada dasarnya merupakan negasi dari sebuah pernyataan. Berikut ini adalah contoh penggunaan operator NOT.

-- Penjelasan
-- Kali ini, aku mengerti bahwa operator NOT di atas akan menghasilkan nilai dimana produk yang dibeli customer tidak sama dengan Jaket.

