SELECT DISTINCT
	*
FROM data_retail
WHERE  Product = "Jaket" AND Average_Transaction_Amount >= 1000000;


Teori
“Untuk keperluan filtering data, operator logika dapat dimasukkan ke dalam perintah WHERE. Dengan menggunakan perintah ini, kita akan mengambil/memilih data berdasarkan operator logika yang digunakan. Coba perhatikan contoh penggunaan operator logika dalam perintah WHERE ini,” Andra menjelaskan.

“Operator di atas akan menghasilkan data dari produk pembelian jaket dan rata-rata jumlah transaksi lebih dari atau sama dengan satu juta” tanyaku setelah menyimak contoh.

